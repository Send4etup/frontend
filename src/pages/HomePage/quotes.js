/**
 * Получить случайную цитату
 * @returns {Object} Объект с текстом цитаты и автором
 */
export const getRandomQuote = () => {
    const randomIndex = Math.floor(Math.random() * EDUCATION_QUOTES.length);
    return EDUCATION_QUOTES[randomIndex];
};

/**
 * Получить цитату дня (фиксированная для каждого дня)
 * @returns {Object} Объект с текстом цитаты и автором
 */
export const getDailyQuote = () => {
    const today = new Date().getDate();
    const quoteIndex = today % EDUCATION_QUOTES.length;
    return EDUCATION_QUOTES[quoteIndex];
};

